export const calculateStats = (data: AnggotaDewan[]): AnggotaStats => {
  const stats: AnggotaStats = {
    total: data.length,
    active: data.filter((a) => a.status === "active").length,
    inactive: data.filter((a) => a.status === "inactive").length,
    byAKD: {},
  };

  data.forEach((a) => {
    a.akd.forEach((akd) => {
      stats.byAKD[akd] = (stats.byAKD[akd] || 0) + 1;
    });
  });

  return stats;
};
